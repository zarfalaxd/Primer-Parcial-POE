    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String numeroCuenta = jTextField5.getText();
        String valorAMoverStr = jTextField6.getText();

        Producto cuenta = buscarCuenta(numeroCuenta);

        if (cuenta != null) {
            try {
                double valorAMover = Double.parseDouble(valorAMoverStr);

                if (valorAMover > 0) {
                    double saldoActual = Double.parseDouble(cuenta.getSaldo());

                    if (valorAMover <= saldoActual) {
                        saldoActual -= valorAMover;

                        cuenta.setSaldo(String.valueOf(saldoActual));

                        mostrarListaCuentas();

                        jTextField5.setText("");
                        jTextField6.setText("");
                    } else {
                        JOptionPane.showMessageDialog(this, "Saldo insuficiente para retirar ese valor.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "El valor a retirar debe ser mayor que 0.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Ingrese un valor válido para retirar.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "La cuenta no existe.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed


    private void ButtoncrearcuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtoncrearcuentaActionPerformed
        String numerocuenta = Fieldnumerocuenta.getText();
        String titular = Fieldtitular.getText();
        String documento = Fielddocumentotitular.getText();
        String saldoStr = Fieldsaldo.getText();

        if (numerocuenta.isEmpty() || titular.isEmpty() || documento.isEmpty() || saldoStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        double saldo;
        try {
            saldo = Double.parseDouble(saldoStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El saldo debe ser un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Producto producto = new Producto(numerocuenta, titular, documento, String.valueOf(saldo));

        listaCuentas.add(producto);

        Fieldnumerocuenta.setText("");
        Fieldtitular.setText("");
        Fielddocumentotitular.setText("");
        Fieldsaldo.setText("");
    }//GEN-LAST:event_ButtoncrearcuentaActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String numeroCuenta = jTextField5.getText();
        String valorAMoverStr = jTextField6.getText();

        Producto cuenta = buscarCuenta(numeroCuenta);

        if (cuenta != null) {
            try {
                double valorAMover = Double.parseDouble(valorAMoverStr);

                if (valorAMover > 0) {
                    double saldoActual = Double.parseDouble(cuenta.getSaldo());
                    saldoActual += valorAMover;

                    cuenta.setSaldo(String.valueOf(saldoActual));

                    mostrarListaCuentas();

                    jTextField5.setText("");
                    jTextField6.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "El valor a consignar debe ser mayor que 0.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Ingrese un valor válido para consignar.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "La cuenta no existe.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


    private Producto buscarCuenta(String numeroCuenta) {
        for (Producto cuenta : listaCuentas) {
            if (cuenta.getNumerocuenta().equals(numeroCuenta)) {
                return cuenta;
            }
        }
        return null;
    }

    private void mostrarListaCuentas() {
        jTextArea1.setText("");

        DecimalFormat decimalFormat = new DecimalFormat("#,##0.###");

        StringBuilder sb = new StringBuilder();
        for (Producto producto : listaCuentas) {
            sb.append("Numero cuenta: ").append(producto.getNumerocuenta()).append("\n");
            sb.append("Nombre Titular: ").append(producto.getTitular()).append("\n");
            sb.append("Documento del titular: ").append(producto.getTipoDocumento()).append("\n");
            sb.append("Saldo: ").append(decimalFormat.format(Double.parseDouble(producto.getSaldo()))).append("\n");
            sb.append("------------\n");
        }

        jTextArea1.setText(sb.toString());
    }

